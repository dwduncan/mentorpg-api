package mil.decea.mentorpgapi.domain.user;
import jakarta.validation.constraints.NotNull;
import mil.decea.mentorpgapi.util.DateTimeAPIHandler;
import java.util.List;
import java.lang.Long;

public record UserRecord(
	int antiguidadeRelativa,
	Titulacao titulacao,
	@NotNull(message="Informe o nome de guerra")
	String nomeGuerra,
	@NotNull(message="Informe o nome completo")
	String nomeCompleto,
	String nomeQualificado,
	Posto posto,
	String especialidade,
	Sexo sexo,
	UserImageRecord userImageRecord,
	@NotNull(message="Informe um CPF v√°lido")
	String cpf,
	String quadro,
	boolean pttc,
	String identidade,
	String observacoes,
	String ultimaPromocao,
	ForcaSingular forcaSingular,
	String dataPraca,
	String role,
	String email,
	String proximaPromocao,
	String saram,
	List<?> documents,
	String dataNascimento,
	String celular,
	Long id,
	boolean ativo) {
	public UserRecord(User obj) {
		this(obj.getAntiguidadeRelativa(),
			obj.getTitulacao(),
			obj.getNomeGuerra(),
			obj.getNomeCompleto(),
			obj.getNomeQualificado(),
			obj.getPosto(),
			obj.getEspecialidade(),
			obj.getSexo(),
			new UserImageRecord(obj.getUserImage()),
			obj.getCpf(),
			obj.getQuadro(),
			obj.isPttc(),
			obj.getIdentidade(),
			obj.getObservacoes(),
			DateTimeAPIHandler.converter(obj.getUltimaPromocao())+"",
			obj.getForcaSingular(),
			DateTimeAPIHandler.converter(obj.getDataPraca())+"",
			obj.getRole(),
			obj.getEmail(),
			DateTimeAPIHandler.converter(obj.getProximaPromocao())+"",
			obj.getSaram(),
			obj.getDocuments(),
			DateTimeAPIHandler.converter(obj.getDataNascimento())+"",
			obj.getCelular(),
			obj.getId(),
			obj.isAtivo());
	}

	public UserRecord(Long id, String cpf, boolean ativo, Posto posto, String quadro, String especialidade, String nomeGuerra, String nomeCompleto) {
		this(0,
			null,
			nomeGuerra,
			nomeCompleto,
			null,
			posto,
			especialidade,
			null,
			null,
			cpf,
			quadro,
			false,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			id,
			ativo);
	}

	public UserRecord(Long id, boolean ativo, String cpf, Titulacao titulacao, Posto posto, String quadro, String especialidade, String nomeGuerra, String nomeCompleto, Sexo sexo, boolean pttc) {
		this(0,
			titulacao,
			nomeGuerra,
			nomeCompleto,
			null,
			posto,
			especialidade,
			sexo,
			null,
			cpf,
			quadro,
			pttc,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			null,
			id,
			ativo);
	}
}